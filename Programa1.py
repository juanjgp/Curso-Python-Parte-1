numero1 = int(input("Introduce el primer número: "))
numero2 = int(input("Introduce el segundo número: "))
numero3 = int(input("Introduce el tercer número: "))

print("El número más grande de los introducidos es: ", max(numero1, numero2, numero3))

print("El números más pequeño de los introducidos es: ", min(numero1, numero2, numero3))